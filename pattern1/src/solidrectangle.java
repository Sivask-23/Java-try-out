public class solidrectangle {
    public static void main(String[] args) {
        //solid rectangle
        int n=5;
        for (int i=1;i<=4;i++) // for row
        {
            for(int j=1;j<=n;j++)// for column
            {
                System.out.print("* ");
            }
            System.out.println("");
        }
        }
    }
